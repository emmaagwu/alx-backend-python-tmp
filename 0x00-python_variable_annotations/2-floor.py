#!/usr/bin/env python3
"""
This module provides a function to calculate the floor of a given number.
"""
import math


def floor(n: float) -> int:
    """
    Returns the floor of a given number.

    Args:
        n (float): The number to be rounded down.

    Returns:
        int: The floor of the given number.
    """
    return math.floor(n)
